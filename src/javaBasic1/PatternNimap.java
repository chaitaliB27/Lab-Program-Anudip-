package javaBasic1;

public class PatternNimap {
public static void main(String[] args) {
	int i,j=0;
	for(i=1;i<=5;i++) {
		for(j=0;j<=5;j++) {
			if(i==1||i==5||j==2||j==4)
			System.out.print("*");
		
		    else {
			System.out.print("");
			}
	
	}System.out.println("");
		
}
}
}