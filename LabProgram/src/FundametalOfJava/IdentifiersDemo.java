package FundametalOfJava;
/*
 * All Java variables must be identified with unique names.
 * These unique names are called identifiers.
 * 
 * The general rules for naming variables are:
 * 1.Names can contain letters, digits, underscores, and dollar signs
 * 2.Names must begin with a letter
 * 3.Names should start with a lower case letter and it cannot contain whitespace
 * 4.Names can also begin with $ and _ .
 * 5.Names are case sensitive.
 * 6.Reserved words cannot be used as names.
 */
public class IdentifiersDemo {

	public static void main(String[] args) {
		int total_number;
		int ca$h;
		int emp_names;
		

	}

}
